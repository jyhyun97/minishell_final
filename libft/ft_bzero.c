#include "libft.h"

void	ft_bzero(void *a, size_t len)
{
	size_t			i;
	unsigned char	*tmp_a;

	i = 0;
	tmp_a = a;
	while (len > i)
	{
		tmp_a[i] = 0;
		i++;
	}
}
